const operacao1 = Number(prompt("Coloque um número par"))
console.log("Número par", operacao1)

const operacao2 = operacao1 / 2
console.log("Dividir por 2", operacao2)

const operacao3 = operacao2  % 2 
console.log("Resto da divisão", operacao3)

// Inserindo-se um número par, o resto da divisão sempre dará 0.

// Inserindo-se um número ímpar, o resto da divisão retornará o 1.
